            <h2>Castor Ocupado (Busy Beaver)</h2>
            <p>O jogo do castor ocupado visa encontrar um programa de terminação de um determinado tamanho que (dependendo da definição) produza a maior saída possível ou seja executado pelo maior número de etapas.</p>
            <p>Um n-ésimo castor ocupado, BB-n ou simplesmente "castor ocupado" é uma máquina de Turing que vence o jogo do castor ocupado de n estados. Dependendo da definição, ele atinge a pontuação mais alta (denotada por Σ(n)), ou é executado pelo maior tempo (S(n)), entre todas as outras possíveis máquinas de Turing concorrentes de n estados</p>
