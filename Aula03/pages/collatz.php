            <h2>Conjectura de Collatz</h2>
            <p>A conjectura de Collatz ​​é um dos problemas não resolvidos mais famosos da matemática. A conjectura pergunta se a repetição de duas operações aritméticas simples acabará por transformar todos os números inteiros positivos em 1.</p>
            <p>Ela se refere a sequências de números inteiros em que cada termo é obtido a partir do termo anterior da seguinte forma: se um termo for par , o próximo termo é metade dele. Se um termo for ímpar, o próximo termo é 3 vezes o termo anterior mais 1.</p>
            <p>A conjectura é que essas sequências sempre chegam a 1, não importa qual número inteiro positivo seja escolhido para iniciar a sequência.</p>
