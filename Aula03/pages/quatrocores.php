            <h2>Teorema das quatro cores</h2>
            <p>Em matemática, o teorema das quatro cores, ou teorema do mapa das quatro cores, afirma que não mais do que quatro cores são necessárias para colorir as regiões de qualquer mapa, de modo que duas regiões adjacentes não tenham a mesma cor.</p>
            <p>A sua formulação é a seguinte: Dado um mapa plano, dividido em regiões, quatro cores são suficientes para colori-lo de forma a que regiões vizinhas não partilhem a mesma cor.</p>
