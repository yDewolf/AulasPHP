            <h2>A conjectura de Golbach</h2>
            <p>A conjetura de Goldbach, proposta pelo matemático prussiano Christian Goldbach, é um dos problemas mais antigos não resolvidos da matemática, mais precisamente da teoria dos números.</p>
            <p>Ela diz que todo número par maior que 2 pode ser representado pela soma de dois números primos. Por exemplo: <br> 4 = 2 + 2; 6 = 3 + 3; 8 = 5 + 3; 10 = 3 + 7 = 5 + 5; 12 = 5 + 7; etc.</p>
