            <h2>O Tijolo de Euler</h2>
            <p>Em matemática, um tijolo de Euler, em homenagem a Leonhard Euler, é um cuboide retangular cujas arestas e diagonais das faces têm comprimentos inteiros.</p>
            <p>Um tijolo de Euler primitivo é um tijolo de Euler cujos comprimentos das arestas são primos. Um tijolo de Euler perfeito é aquele cuja diagonal espacial também é um inteiro, mas tal tijolo ainda não foi encontrado.</p>
